package main.java.tasks.collections;


public  class WaterNew {

    private String color = "no";
    private String smell = "no";


    public WaterNew(String color, String smell) {
        this.color = color;
        this.smell = smell;
    }

    public String getColor() {
        return color;
    }
    public void setColor(String color) {
        this.color = color;
    }
    public String isSmell() {
        return smell;
    }
    public void setSmell(String smell) {
        this.smell = smell;
    }

}
